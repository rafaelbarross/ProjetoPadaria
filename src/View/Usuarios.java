    private void cadastrar_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_usuarioActionPerformed
        // TODO add your handling code here:
        
        
        
        int codigo_de_usuario = Integer.parseInt(this.codigo_de_usuario.getText());
        String nome = this.nome.getText();
        String nome_de_usuario = this.nome_de_usuario.getText();
        String senha = this.senha.getText();
        
        Usuario u = new Usuario(codigo_de_usuario,nome,nome_de_usuario,senha);
        Usuarios_DAO ubanco = new Usuarios_DAO(conexao2);
        ubanco.cadastrar(u);
        
        this.codigo_de_usuario.setText("");
        this.nome.setText("");
        this.nome_de_usuario.setText("");
        this.senha.setText("");
        
        this.nome.setEnabled(false);
        this.nome_de_usuario.setEnabled(false);
        this.senha.setEnabled(false);
        cadastrar_usuario.setEnabled(false);
        consultar_usuario.setEnabled(true);
        excluir_usuario.setEnabled(true);
    }//GEN-LAST:event_cadastrar_usuarioActionPerformed

    private void consultar_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultar_usuarioActionPerformed
        // TODO add your handling code here:
        
        
        int codigo_de_usuario = Integer.parseInt(this.codigo_de_usuario.getText());
        
        
        Usuarios_DAO p = new Usuarios_DAO(conexao2);
        Usuario usuario = p.Consultar(codigo_de_usuario);
        
        if(usuario != null){
            this.nome.setText(usuario.getNome());
            this.nome_de_usuario.setText(usuario.getNome_de_usuario());
            this.senha.setText(usuario.getSenha());
        }else{
            int op = JOptionPane.showConfirmDialog(this, "O código do usuario não existe!\ntente casdatrar um novo usuario!","casdatrar um novo usuario?", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(op == 0){
        nome.setEnabled(true);
        nome_de_usuario.setEnabled(true);
        senha.setEnabled(true);
        cadastrar_usuario.setEnabled(true);
        excluir_usuario.setEnabled(false);
        consultar_usuario.setEnabled(false);
         this.codigo_de_usuario.setText("");
         this.nome.setText("");
         this.nome_de_usuario.setText("");
         this.senha.setText("");
            }
         this.codigo_de_usuario.setText("");

        }
    }//GEN-LAST:event_consultar_usuarioActionPerformed

