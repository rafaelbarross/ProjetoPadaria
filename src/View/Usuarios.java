    private void excluir_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluir_usuarioActionPerformed
        // TODO add your handling code here:
        int codigo_de_usuario = Integer.parseInt(this.codigo_de_usuario.getText());
        
        Usuarios_DAO u = new Usuarios_DAO(conexao2);
        
        if (u.Consultar(codigo_de_usuario) != null) {
            u.excluir(codigo_de_usuario);
        }else{
            JOptionPane.showMessageDialog(null, "O campo de código está vazio.");
        }
        this.codigo_de_usuario.setText("");
    }//GEN-LAST:event_excluir_usuarioActionPerformed

